package com.zhixiangli.gomoku.chessboard;

/**
 * chessboard's state.
 * 
 * @author lizhixiang
 */
public enum ChessState {

    /**
     * game is on.
     */
    GAME_ON,

    /**
     * game is draw.
     */
    GAME_DRAW,

    /**
     * white player win.
     */
    WHITE_WIN,

    /**
     * black player win.
     */
    BLACK_WIN,

}
